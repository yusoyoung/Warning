public class Calculator {

    public long add(int a, int b){
        return (long)a+b;
    }

    public long multiply(int c, int d){
        return (long)c*d;
    }

    public long divide(int e, int f){
        return (long)e/f;
    }

    public long substract(int g, int h){
        return (long)g-h;
    }
}
